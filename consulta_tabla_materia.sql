-- Ver estructura de la tabla materia
SELECT 
    column_name,
    data_type,
    is_nullable,
    column_default,
    character_maximum_length
FROM information_schema.columns 
WHERE table_name = 'materia' 
ORDER BY ordinal_position;

-- Ver todos los datos de la tabla materia
SELECT * FROM materia;

-- Ver constraints de la tabla materia
SELECT 
    tc.constraint_name,
    tc.constraint_type,
    kcu.column_name
FROM information_schema.table_constraints AS tc
JOIN information_schema.key_column_usage AS kcu
    ON tc.constraint_name = kcu.constraint_name
WHERE tc.table_name = 'materia';